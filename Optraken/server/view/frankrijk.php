        <h2>Route </h2>
        <p>Komende vanuit Châtel kom je via een rotonde La Chapelle in en rijd je op een gegeven moment onder de cabinelift La Panthiaz door. 
            Iets verderop zie je rechts restaurant Le Fer Rouge. Ongeveer 100 m verderop zie je links een TABAC SOUVENIRS en rechts een dikke stenen muur. Hier moet je rechtsaf. Er staan bordjes met: HOTEL LE CHABI en Le Rys. Het is steil, maar met goede winterbanden kom je boven. 
            Er volgt een S-bocht en daarna zie je links hotel Le Chabi. Na dat hotel volgt een groot houten chalet, daarna een ander donker chalet. Het volgende gebouw (het derde dus) is L'Op-traken. Rij er voorbij, maak de scherpe bocht naar links en ga in de bocht meteen linksaf het parkeerterrein op.
             De parkeerplaatsen zijn genummerd. 
            De parkeerplaats van L’Op-traken nummer 8 ligt rechts aan de kant van de berg. Anderen mogen niet op die plek staan.
             Als dat toch het geval is, dan moet je tijdelijk op een andere vrije plek parkeren.
            Het eerste gebouw dat je lopend nadert, is Le Télémark. Loop door tot het tweede gebouw, dat is L'Op-traken. Je kan de bagage dragen of je kan er achteruit naartoe rijden om uit te laden. Nummer 8 is meteen linksaf.
